type Signal int

const (
	Waiting Signal = iota
	Started
	Complete
	Abort
	AbortRetry
	// TODO: PartialAbort (??)
)

type NodeType int

const (
	UINode NodeType = iota
	TemporalNode
	VUINode
	VirtualNode
)

// Dependency Graph Node
// every DGNode has an id, a Type, a state, and a set of Access Procedures
type DGNode interface {
	ID() int
	Type() NodeType // specifies whether node is UI, VUI, etc.
	State() Signal
	Priority() int // not necessary, but can be useful
	ListProcedures() ProcedureList
	Dependents() []DGNode   // often just a `return MyNode.Dependents`
	Dependencies() []DGNode // often just a `return MyNode.Dependencies`
	IsRootBoundary() bool   // Autogenerated
	IsLeafBoundary() bool   // Autogenerated
}

